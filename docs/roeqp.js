class RoeqpItem {
  id;
  pos;
  name;
  refined = 0;

  constructor(id, pos, name, refined) {
    this.id = id
    this.pos = pos
    this.name = name
    this.refined = refined
  }

  label() {
    if (this.refined > 0) {
      return `+${this.refined}${this.name}`
    } else {
      return this.name
    }
  }
}

Roeqp.debug = function(string) {
  console.debug(string)
}

Roeqp.scan = function(text) {
  // roratorio補正: "【習】剛勇無双の神輿"
  text = text.replace(/【習】/g,'')

  const items = [];
  const match = text.match(this.regexp);
  
  // "もふ"     .match(/^(\+(\d+))?\s*(.*)/)  // => ['もふ', undefined, undefined, 'もふ', ...
  // "+5もふ"   .match(/^(\+(\d+))?\s*(.*)/)  // => ['+5もふ', '+5', '5', 'もふ', ...
  // "+5 \nもふ".match(/^(\+(\d+))?\s*(.*)/)  // => ['+5 \nもふ', '+5', '5', 'もふ', ...
  match?.forEach(full => {
    this.debug(`regexp match: ${full}`)
    const md = full.match(/^(\+(\d+))?\s*(.*)/)	// '+5 \nもふ'
    // ['+5 \nもふ', '+5', '5', 'もふ', ...
    //      0         1     2     3
    const refined = Number(md[2])||0  // => 2
    const name    = md[3]	      // => 'もふ'
    const tuple2 = this.dict[name]    // => [0,19497]
    const pos    = tuple2[0]	      // => 0
    const id     = tuple2[1]	      // => 19497
    if (tuple2) {
      items.push(new RoeqpItem(id, pos, name, refined))
    }
  });

  return items
};

Roeqp.build_regexp = function(dict){
  let source = '';
  for (let name in dict) {
    if (source !== '') source = source.concat('|');
    source = source.concat(name);
  }

  // 精錬値対応: "+10 ", "+5"
  // ```js
  // "+10 \nファントムオブマスカレード +5もふもふお".match(new RegExp("(\\+\\d+\\s*)?(" + "ファントムオブマスカレード|もふもふ" + ")","gu"))
  // // => ['+10 \nファントムオブマスカレード', '+5もふもふ']
  // ```
  return new RegExp("(\\+\\d+\\s*)?(" + source + ")", "gu");
}

Roeqp.regexp = Roeqp.build_regexp(Roeqp.dict);
